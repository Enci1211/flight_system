

/* * * * * * * * * * * * * * * * * * * * *  CREATE TABLE STRUCTURE  * * * * * * * * * * * * * * * * * * * */

CREATE TABLE IF NOT EXISTS aircraft
(
RegMark VARCHAR(6) PRIMARY KEY NOT NULL,
Manufacturer VARCHAR(20) NOT NULL,
Model VARCHAR(20) NOT NULL,
Seating INT NOT NULL,
MinRunwayLength INT NOT NULL
);

CREATE TABLE IF NOT EXISTS airport
(
AirportCode VARCHAR(3) PRIMARY KEY NOT NULL,
AirportName VARCHAR(50) NOT NULL,
MaxRunwayLength INT NOT NULL
);

CREATE TABLE IF NOT EXISTS staff
(
StaffID INT PRIMARY KEY NOT NULL AUTO_INCREMENT,
FirstName VARCHAR(25) NOT NULL,
LastName VARCHAR(25) NOT NULL,
EmailAddress VARCHAR(50) NOT NULL,
PhoneNumber VARCHAR(20) NOT NULL,
PassportNumber VARCHAR(15) NOT NULL,
DateOfBirth DATE NOT NULL,
IsManager BOOLEAN NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS route
(
FlightNum VARCHAR(6) PRIMARY KEY NOT NULL,
DepCode VARCHAR(3) NOT NULL,
ArrCode VARCHAR(3) NOT NULL,
MasterRoute VARCHAR(7) NOT NULL,
FOREIGN KEY (ArrCode) REFERENCES airport(AirportCode)
	ON UPDATE CASCADE
	ON DELETE CASCADE,
FOREIGN KEY (DepCode) REFERENCES airport(AirportCode)
	ON UPDATE CASCADE
	ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS status
(
FlightStatus VARCHAR(10) PRIMARY KEY NOT NULL,
StatusDesc VARCHAR(255) NULL
);

CREATE TABLE IF NOT EXISTS flight
(
FlightID INT PRIMARY KEY NOT NULL AUTO_INCREMENT,
FlightNum VARCHAR(10) NOT NULL,
WeekNum INT NOT NULL,
FlightDate DATE NOT NULL,
DepTime TIME NOT NULL,
ArrTime TIME NOT NULL,
Duration TIME NOT NULL,
DepEstAct TIME NULL,
ArrEstAct TIME NULL,
FlightStatus VARCHAR(10) NOT NULL,
Aircraft VARCHAR(6) NOT NULL,
FOREIGN KEY (Aircraft) REFERENCES aircraft(RegMark)
	ON UPDATE CASCADE
	ON DELETE CASCADE,
FOREIGN KEY (FlightNum) REFERENCES route(FlightNum)
	ON UPDATE CASCADE
	ON DELETE CASCADE,
FOREIGN KEY (FlightStatus) REFERENCES status(FlightStatus)
	ON UPDATE CASCADE
	ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS passenger
(
PassengerID INT PRIMARY KEY NOT NULL AUTO_INCREMENT,
FirstName VARCHAR(25) NOT NULL,
LastName VARCHAR(25) NOT NULL,
EmailAddress VARCHAR(50) NOT NULL,
PhoneNumber VARCHAR(20) NOT NULL,
PassportNumber VARCHAR(15) NOT NULL,
DateOfBirth DATE NOT NULL,
LoyaltyTier TINYINT DEFAULT 1
 );

CREATE TABLE IF NOT EXISTS passengerFlight
(
FlightID INT NOT NULL,
PassengerID INT NOT NULL,
CONSTRAINT PK_passengerFlight PRIMARY KEY (FlightID,PassengerID),
FOREIGN KEY (PassengerID) REFERENCES passenger(PassengerID)
	ON UPDATE CASCADE
	ON DELETE CASCADE,
FOREIGN KEY (FlightID) REFERENCES flight(FlightID)
	ON UPDATE CASCADE
	ON DELETE CASCADE
);

